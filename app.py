def clean_text(text):
    text = text.lower()
    text = re.sub(r"http\S+|www\.\S+", "", text)
    text = re.sub(r"[^a-z\s<>_/:-]", "", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text

def trim_response(text, max_words=120):
    words = text.split()
    return text if len(words) <= max_words else " ".join(words[:max_words]) + "..."

